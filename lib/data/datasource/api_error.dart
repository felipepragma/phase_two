///
/// [ApiError] Enum to api error
/// 
/// [requestTimeout] : 408
/// [unauthorized] : 401
/// [notFound] : 404
/// [serverError] : 500
/// [noInternetConnection] : -1
/// [unknown] : -1
/// 
enum ApiError {
  requestTimeout,
  unauthorized,
  notFound,
  serverError,
  noInternetConnection,
  unknown,
}